package relacionamentoUML;

public class Carro {
	private String nomeCarro ;
	private String descricaoProblemas ;
	private Float valorMaoObra ;
	private Float valorpessaRepor ;
	private Float valorPrevio;
	
	
	
	public String getNomeCarro() {
		return nomeCarro;
	}
	public void setNomeCarro(String nomeCarro) {
		this.nomeCarro = nomeCarro;
	}
	public String getDescricaoProblemas() {
		return descricaoProblemas;
	}
	public void setDescricaoProblemas(String descricaoProblemas) {
		this.descricaoProblemas = descricaoProblemas;
	}
	public Float getValorMaoObra() {
		return valorMaoObra;
	}
	public void setValorMaoObra(Float valorMaoObra) {
		this.valorMaoObra = valorMaoObra;
	}
	public Float getValorpessaRepor() {
		return valorpessaRepor;
	}
	public void setValorpessaRepor(Float valorpessaRepor) {
		this.valorpessaRepor = valorpessaRepor;
	}
	public Float getValorPrevio() {
		return valorPrevio;
	}
	public void setValorPrevio(Float valorPrevio) {
		this.valorPrevio = valorPrevio;
	}
	

}
